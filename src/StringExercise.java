public class StringExercise {
    public static void main(String[] args) {

//      OUTPUT 1
        String outputOne = "We don't need no education\nWe don't need no thought control";
        System.out.println(outputOne);

//      OUTPUT 2
        String outputTwo = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(outputTwo);

//      OUTPUT 3
        String outputThree = "In windows, the main drive is usually C:\\";
        System.out.println(outputThree);

//      OUTPUT 4
        String outputFour = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(outputFour);
    }
}
